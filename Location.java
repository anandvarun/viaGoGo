public class Location{
    
    private float x;
    private float y;
    
    Location(float x,float y){
        
    	this.x = x;
    	this.y = y;
        
    }
    
    float getX(){
        
        return this.x;
        
    }
    
    float getY(){
        
        return this.y;
        
    }
    
}
